import java.util.Scanner; 
public class BuyuktenKucugeSiralama {
		
		public static void main(String[] args) {
			/*Sayýlarý Büyükten Küçüðe Sýralayan Program
			Koþullu ifadeler ile girilen 3 sayýyý büyükten küçüðe sýralayan program */
			
			Scanner input=new Scanner(System.in);
			int a,b,c;
			
			System.out.println("Birinci sayý: ");
			a=input.nextInt();
			System.out.println("Ýkinci sayý: ");
			b=input.nextInt();
			System.out.println("Üçüncü sayý: ");
			c=input.nextInt();
			
			if(a>b && a>c) {
				if(b>c) {System.out.println(a+">"+b+">"+c);}
				else {System.out.println(a+">"+c+">"+b);}
			}
			
			else if(b>a && b>c){
				if(a>c) {System.out.println(b+">"+a+">"+c);}
				else {System.out.println(b+">"+c+">"+a);}
			}
			else if (c>a && c>b){
				if(a>b) {System.out.println(c+">"+a+">"+b);}
				else {System.out.println(c+">"+b+">"+a);}	
			}
			else System.out.println("Lütfen birbirine eþit deðerler girmeyin");

		}

	}


